public class p2 {
    public static void main(String[] args) {


        int a = 10, b = 10;
        System.out.println("Int Addition: " + (a + b));
        System.out.println("Int Subtraction: " + (a - b));
        System.out.println("Int Multiplication: " + (a * b));
        System.out.println("Int Division: " + (a / b));

        float x = 10.5f, y = 5.5f;
        System.out.println("Float Addition: " + (x + y));
        System.out.println("Float Subtraction: " + (x - y));
        System.out.println("Float Multiplication: " + (x * y));
        System.out.println("Float Division: " + (x / y));

        double p = 10.5, q = 4.5;
        System.out.println("Double Addition: " + (p + q));
        System.out.println("Double Subtraction: " + (p - q));
        System.out.println("Double Multiplication: " + (p * q));
        System.out.println("Double Division: " + (p / q));
    }
    
}
