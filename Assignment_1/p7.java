public class p7 {
    public static void main(String[] args) {

        var avg = true;
        System.out.println("Boolean var: " + avg);

        var num = 100;
        System.out.println("Int var: " + num);

        var value = 10.5f;
        System.out.println("Float var: " + value);

        int var = 50;
        System.out.println("User-defined variable name 'var': " + var);
    }
    
}
