public class p4 {
public static void main(String[] args) {
        int i = 110;
        byte b = (byte) i;

        double d = 25.55;
        int intVal = (int) d;
        byte byteVal = (byte) d;

        System.out.println("Int to Byte: " + b);
        System.out.println("Double to Int: " + intVal);
        System.out.println("Double to Byte: " + byteVal);
}
}